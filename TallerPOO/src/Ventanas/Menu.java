    private void mostrarDatosPropietarios() {
        String fichero = "";

        try {
            BufferedReader br = new BufferedReader(new FileReader("Propietarios_Apartamentos.json"));
            String linea;
            while ((linea = br.readLine()) != null) {
                fichero += linea;
            }

            if (!fichero.isEmpty()) {
                ListaPropietarios abrir = new ListaPropietarios();
                this.setVisible(false);
                abrir.setVisible(true);
            } else {
                JOptionPane.showConfirmDialog(null, "No se han registrado datos", "Precauci贸n", JOptionPane.CLOSED_OPTION);
            }

        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error Fichero no encontrado", 0);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", 0);
        }
    }

